    private void jbtn_AddNVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_AddNVActionPerformed
            String MaNV = jtxt_MaNV.getText();
            String TenNV = jtxt_TenNV.getText();
            String SDT = jtxt_sđtNV.getText();
//          int SDT = Integer.parseInt(jtxt_sđtNV.getText());
            String DiaChi = jtxt_DiaChiNV.getText();
            int ChucVu = Integer.parseInt(jSpinner_ChucVu.getValue().toString());           
            String NgayNC = jtxt_NgayNhanChuc.getText();
        if (MaNV.equals("")) {
            JOptionPane.showConfirmDialog(this, "Vui lòng nhập vào mã nhân viên!", "Thông báo",
                    JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
        }
        else if (TenNV.equals("")) {
            JOptionPane.showConfirmDialog(this, "Vui lòng nhập vào tên nhân viên!", "Thông báo", 
                    JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
        }
        else if (SDT.equals("")) {
            JOptionPane.showConfirmDialog(this, "Vui lòng nhập số điện thoại nhân viên!", "Thông báo", 
                    JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
        }
        else if (SDT.length() != 10) {
            JOptionPane.showConfirmDialog(this, "Số điện thoại phải 10 chữ số!", "Thông báo", 
                    JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
        }
        else if (DiaChi.equals("")) {
            JOptionPane.showConfirmDialog(this, "Vui lòng nhập vào địa chỉ nhân!", "Thông báo", 
                    JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
        }
        else if (NgayNC.equals("")) {
            JOptionPane.showConfirmDialog(this, "Vui lòng nhập ngày nhận chức!", "Thông báo", 
                    JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
        }
        else{
        try {
                
                PreparedStatement pre = MysqlConn.nonQuery("insert into nhanvien(MaNV, TenNV, SDT, DiaChi, ChucVu, NgayNC, matkhau) "
                        + "values (?, ?, ?, ?, ?, ?, ?)",MaNV,TenNV,SDT,DiaChi,ChucVu,NgayNC,SDT);
                int roweff = pre.executeUpdate();
                if (roweff > 0) {
                    LoadData();
                    JOptionPane.showConfirmDialog(this, "Dữ liệu đã được thêm vào!", "Thông Báo", 
                            JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE );
                }                
        } catch (SQLException e) {
            e.printStackTrace();
        }
        }
    }//GEN-LAST:event_jbtn_AddNVActionPerformed


    private void LoadData() {
        try {
                ResultSet rs = MysqlConn.query("SELECT MaNV,TenNV, concat('0', sdt) as sdt, DiaChi, ChucVu, NgayNC FROM nhanvien");
                DefaultTableModel model = (DefaultTableModel)jTable_NhanVien.getModel();
                model.setRowCount(0);
                while(rs.next()){
                model.addRow(new Object[]{
                    rs.getInt("MaNV"),
                    rs.getString("TenNV"),
                    rs.getString("SDT"),
                    rs.getString("DiaChi"),
                    rs.getByte("ChucVu"),
                    rs.getString("NgayNC")});
                }
                
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, e);
        }      
    }
    

    private void jbtn_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_DeleteActionPerformed
            String MaKH = jtxt_MaKH.getText();
            String TenKH = jtxt_TenKH.getText();
            String DiaChi = jtxt_DiaChi.getText();
            String TongTien = jtxt_TongTien.getText();
        if (MaKH.equals("")) {
            JOptionPane.showConfirmDialog(this, "Vui lòng chọn khách hàng!", "Thông báo",
                    JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
        }
        else if (TenKH.equals("")) {
            JOptionPane.showConfirmDialog(this, "Vui lòng chọn khách hàng!", "Thông báo", 
                    JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
        }
        else if (DiaChi.equals("")) {
            JOptionPane.showConfirmDialog(this, "Vui lòng chọn khách hàng!", "Thông báo", 
                    JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
        }
        else if (TongTien.equals("")) {
            JOptionPane.showConfirmDialog(this, "Vui lòng chọn khách hàng!", "Thông báo", 
                    JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
        }
        else{  
        try {
                PreparedStatement pre = MysqlConn.nonQuery("delete from khachhang where MaKH = ? ",MaKH);    
                if (JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn xoá dữ liệu này", "Thông báo", 
                         JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE ) == JOptionPane.NO_OPTION) {                   
                    return;
                }
                
                int roweff = pre.executeUpdate();
                if (roweff > 0) {
                    LoadData();
                    JOptionPane.showConfirmDialog(this, "Dữ liệu đã được xoá!", "Thông Báo", 
                            JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE );
            }
                
        } catch (SQLException e) {
            e.printStackTrace();
        }
        }
    }//GEN-LAST:event_jbtn_DeleteActionPerformed


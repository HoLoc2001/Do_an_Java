    private void LoadData() {
        try {
                ResultSet rs = MysqlConn.query("select * from khachhang");
                DefaultTableModel model = (DefaultTableModel)jTable_KhachHang.getModel();
                model.setRowCount(0);
                while (rs.next()) {    
                    model.addRow(new Object[]{  
                        rs.getString("MaKH"),
                        rs.getString("TenKH"),
                        rs.getString("DiaChi"),
                        rs.getString("TongTien")});  
                }
                
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, e);
        }      
    }
    

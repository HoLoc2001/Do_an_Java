    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!lbTenKH.getText().equals("")) {
            jfHoaDon f = new jfHoaDon();
        f.setVisible(true);
        f.tbBillClone.setModel(tbBill.getModel());
        f.tbBillClone.getColumnModel().getColumn(0).setPreferredWidth(400);
        f.tbBillClone.getColumnModel().getColumn(1).setPreferredWidth(30);
        f.tbBillClone.getColumnModel().getColumn(2).setPreferredWidth(40);
        f.tbBillClone.getColumnModel().getColumn(3).setPreferredWidth(40);
        f.tbBillClone.setAutoResizeMode(JTable.AUTO_RESIZE_LAST_COLUMN);
        f.lbTongCong.setText(txtTongCong.getText() + " VNĐ");
        f.lbTenNV.setText(lbTenNV.getText());
        f.lbSL.setText(lbSLtong.getText());
        try {
                ResultSet rs = MysqlConn.query("select * from khachhang where makh = ?", txtMaKH.getText());
                String a = txtTongCong.getText().replace(",", "");
                while (rs.next()) {
                f.lbTenKH.setText(rs.getString("MaKH"));
                f.lbSDT_KH.setText(rs.getString("tenKH"));
                f.lbDC_KH.setText(rs.getString("diachi"));
                };
                PreparedStatement pre1 = MysqlConn.nonQuery("{ call USP_HoaDon(?, ?, ?, ?) }", txtMaKH.getText(), lbTenNV.getText(), lbSLtong.getText(), Long.parseLong(a));
                pre1.executeUpdate();
                for(int i = 0; i < tbBill.getRowCount(); i++)
                {   
                    String str = tbBill.getValueAt(i, 3).toString();
                    PreparedStatement pre = MysqlConn.nonQuery("{ call USP_CT_HoaDon(?,?,?)}", tbBill.getValueAt(i, 0), tbBill.getValueAt(i, 1), Integer.parseInt(str.replace(",", "")));
                    int roweff = pre.executeUpdate();
                }
                
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showConfirmDialog(this, e, "Thông báo", 
                    JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
        }
        }else{
            JOptionPane.showConfirmDialog(this, "Vui lòng nhập mã khách hàng", "Thông báo", 
                    JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    

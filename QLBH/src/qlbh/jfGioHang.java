    private void tbBillMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbBillMouseClicked
        jfShowItem f = new jfShowItem();
        Integer i = tbBill.getSelectedRow();
        txtBillTenSP.setText(tbBill.getModel().getValueAt(i, 0).toString());
        txtDonGia.setText(tbBill.getModel().getValueAt(i, 2).toString());
        Integer sl1 = f.findSLItem(txtBillTenSP.getText());
        spBillSL.setModel(new SpinnerNumberModel((int)tbBill.getModel().getValueAt(i, 1), 1,(int) sl1 , 1));
        txtThanhTien.setText(tbBill.getModel().getValueAt(i, 3).toString());
    }//GEN-LAST:event_tbBillMouseClicked
    
    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        DefaultTableModel tbmodel = (DefaultTableModel) tbBill.getModel();
        if (tbBill.getSelectedRowCount() == 1) {
            tbmodel.removeRow(tbBill.getSelectedRow());
            txtBillTenSP.setText("");
            txtDonGia.setText("");
            txtThanhTien.setText("");
            spBillSL.setValue(0);
        }
        loadTongTien();
        
    }//GEN-LAST:event_btnXoaActionPerformed
    public boolean isSua = false;
    public Hashtable<String, Integer> hashtable = new Hashtable<String, Integer>();
    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        isSua = true;
        hashtable.put(txtBillTenSP.getText(), (Integer)spBillSL.getValue() - (Integer)tbBill.getValueAt(tbBill.getSelectedRow(), 1));
        tbBill.setValueAt(spBillSL.getValue(), tbBill.getSelectedRow(), 1);
        String str = tbBill.getValueAt(tbBill.getSelectedRow(), 2).toString();
        String a = str.replace(",", "");
        DecimalFormat formatter = new DecimalFormat("###,###,###,###");
        tbBill.setValueAt(formatter.format((Integer)spBillSL.getValue() * Integer.parseInt(a.trim())), tbBill.getSelectedRow(), 3);
        loadTongTien();
    }//GEN-LAST:event_btnSuaActionPerformed

